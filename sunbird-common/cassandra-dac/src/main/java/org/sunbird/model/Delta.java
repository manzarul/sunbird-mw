package org.sunbird.model;

public class Delta {
	
	private String contentId;
	private String prevContentId;
	private String nextContentId;
	private String description;
	private String createdDate;
	private String createdBy;
	/**
	 * @return the contentId
	 */
	public String getContentId() {
		return contentId;
	}
	/**
	 * @param contentId the contentId to set
	 */
	public void setContentId(String contentId) {
		this.contentId = contentId;
	}
	/**
	 * @return the prevContentId
	 */
	public String getPrevContentId() {
		return prevContentId;
	}
	/**
	 * @param prevContentId the prevContentId to set
	 */
	public void setPrevContentId(String prevContentId) {
		this.prevContentId = prevContentId;
	}
	/**
	 * @return the nextContentId
	 */
	public String getNextContentId() {
		return nextContentId;
	}
	/**
	 * @param nextContentId the nextContentId to set
	 */
	public void setNextContentId(String nextContentId) {
		this.nextContentId = nextContentId;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the createdDate
	 */
	public String getCreatedDate() {
		return createdDate;
	}
	/**
	 * @param createdDate the createdDate to set
	 */
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	/**
	 * @return the createdBy
	 */
	public String getCreatedBy() {
		return createdBy;
	}
	/**
	 * @param createdBy the createdBy to set
	 */
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	
	
	

}
